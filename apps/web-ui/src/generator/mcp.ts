// --- server ----------------------------------------------------------------

function serverTs(cfg: HarnessConfig, remote: boolean): string {
  const transport = remote
    ? `  // Streamable HTTP transport for hosted / team / enterprise deployments.
  // Every request is authenticated before any tool is dispatched.
  const { authenticate } = await import('./auth.js');
  void authenticate; // wire into your HTTP framework's request handler`
    : `  // stdio transport — the default for Claude Code / desktop agent workflows.`;
  return `${HEADER}
import { TOOLS } from './tools.js';
import { RESOURCES } from './resources.js';
import { PROMPTS } from './prompts.js';
import { decide, withTimeout, POLICY } from './policy.js';
import { audit } from './audit.js';

/**
 * MCP server for ${cfg.name} (${remote ? 'remote / Streamable HTTP' : 'local / stdio'}).
 * JSON-RPC 2.0. This is the gated dispatch path: tool -> policy -> (approval) ->
 * timeout-bounded run -> audit. Wire it to your transport of choice.
 */
export async function start(): Promise<void> {
${transport}
  let callsThisTurn = 0;

  process.on('SIGINT', () => process.exit(0));
  console.error('[${cfg.name}] MCP server ready —', TOOLS.length, 'tools,', RESOURCES.length, 'resources,', PROMPTS.length, 'prompts');
  // (Dispatch loop omitted from the scaffold — call dispatch() from your transport.)
  void start;
  // expose for the transport:
  void callsThisTurn;
}

export async function dispatch(toolName: string, args: Record<string, unknown>): Promise<unknown> {
  const tool = TOOLS.find((t) => t.name === toolName);
  if (!tool) {
    await audit({ ts: new Date().toISOString(), tool: toolName, decision: 'error', reason: 'unknown tool' });
    throw new Error('unknown tool: ' + toolName);
  }
  const d = decide(tool.ctx);
  if (!d.allowed) {
    await audit({ ts: new Date().toISOString(), tool: toolName, decision: 'denied', reason: d.reason });
    throw new Error('policy denied ' + toolName + ': ' + d.reason);
  }
  if (d.requiresApproval) {
    await audit({ ts: new Date().toISOString(), tool: toolName, decision: 'approval-required', reason: 'dangerous tool' });
    throw new Error('tool ' + toolName + ' requires user approval (policy.requireApprovalForDangerous)');
  }
  const startedAt = Date.now();
  const out = await withTimeout(() => tool.run(args));
  await audit({ ts: new Date().toISOString(), tool: toolName, decision: 'allowed', reason: 'ok', durationMs: Date.now() - startedAt });
  return out;
}

if (import.meta.url === \`file://\${process.argv[1]}\`) {
  start().catch((e) => {
    console.error(e);
    process.exit(1);
  });
}

void POLICY;
`;
}

// --- data artifacts --------------------------------------------------------

function policyJson(p: McpPolicy): string {
  return JSON.stringify(p, null, 2) + '\n';
}

function capabilitiesJson(cfg: HarnessConfig): string {
  const remote = cfg.primitives.mcp === 'remote';
  return (
    JSON.stringify(
      {
        schema: 'mcp-capabilities/v1',
        harness: cfg.name,
        mode: cfg.primitives.mcp,
        transport: remote ? 'streamable-http' : 'stdio',
        auth: remote ? 'bearer' : 'none',
        tools: ['ping'],
        resources: ['harness://manifest', 'harness://capabilities'],
        prompts: ['kickoff'],
        policy: cfg.mcpPolicy,
      },
      null,
      2,
    ) + '\n'
  );
}
