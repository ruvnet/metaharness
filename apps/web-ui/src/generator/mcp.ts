// The MCP primitive — modular, gated, security-first (ADR-022).
//
// MCP is ONE selectable primitive, not the product. When enabled it emits a
// small, self-contained MCP surface into the harness: a JSON-RPC server
// (stdio for `local`, Streamable HTTP for `remote`), deterministic tool /
// resource / prompt registries, an enforced security policy, an append-only
// audit log, and (remote only) an auth layer. Every capability is default-deny:
// the author opts INTO network / shell / file-write, never out of safety.
//
// The policy is emitted twice — as enforced TS (policy.ts) and as inert data
// (.harness/mcp-policy.json) — so it can be scanned (`harness mcp-scan`),
// audited, and folded into the witness manifest.

import type { GenFile, HarnessConfig, McpPolicy } from './types';

const HEADER = '// SPDX-License-Identifier: MIT\n// Generated by agent-harness-generator — MCP primitive (ADR-022).\n';

export function mcpFiles(cfg: HarnessConfig): GenFile[] {
  if (cfg.primitives.mcp === 'off') return [];
  const remote = cfg.primitives.mcp === 'remote';
  const files: GenFile[] = [
    { path: 'src/mcp/server.ts', content: serverTs(cfg, remote) },
    { path: 'src/mcp/tools.ts', content: toolsTs(cfg) },
    { path: 'src/mcp/resources.ts', content: resourcesTs(cfg) },
    { path: 'src/mcp/prompts.ts', content: promptsTs(cfg) },
    { path: 'src/mcp/policy.ts', content: policyTs() },
    { path: 'src/mcp/audit.ts', content: auditTs() },
    { path: '.harness/mcp-policy.json', content: policyJson(cfg.mcpPolicy) },
    { path: '.harness/mcp-capabilities.json', content: capabilitiesJson(cfg) },
  ];
  if (remote) files.push({ path: 'src/mcp/auth.ts', content: authTs() });
  return files;
}

// --- enforced policy (TS) --------------------------------------------------

function policyTs(): string {
  return `${HEADER}
import policyData from '../../.harness/mcp-policy.json' with { type: 'json' };

export interface ToolCtx {
  /** Declared capabilities this tool needs. */
  needs: { network?: boolean; shell?: boolean; fileWrite?: boolean };
  /** Author/host has flagged this tool dangerous. */
  dangerous?: boolean;
}

export interface PolicyDecision {
  allowed: boolean;
  reason: string;
  requiresApproval: boolean;
}

export const POLICY = policyData as {
  defaultDeny: boolean;
  allowNetwork: boolean;
  allowShell: boolean;
  allowFileWrite: boolean;
  requireApprovalForDangerous: boolean;
  toolTimeoutMs: number;
  maxToolCallsPerTurn: number;
  auditLog: boolean;
};

/**
 * The single gate every tool call passes through. Default-deny: a capability
 * the policy does not grant is refused, not silently allowed.
 */
export function decide(ctx: ToolCtx): PolicyDecision {
  if (ctx.needs.network && !POLICY.allowNetwork) {
    return { allowed: false, reason: 'network not permitted by policy', requiresApproval: false };
  }
  if (ctx.needs.shell && !POLICY.allowShell) {
    return { allowed: false, reason: 'shell not permitted by policy', requiresApproval: false };
  }
  if (ctx.needs.fileWrite && !POLICY.allowFileWrite) {
    return { allowed: false, reason: 'file-write not permitted by policy', requiresApproval: false };
  }
  const requiresApproval = !!ctx.dangerous && POLICY.requireApprovalForDangerous;
  return { allowed: true, reason: 'permitted', requiresApproval };
}

/** Wrap an async tool body with the policy's per-call timeout. */
export async function withTimeout<T>(fn: () => Promise<T>): Promise<T> {
  let timer: ReturnType<typeof setTimeout>;
  const timeout = new Promise<never>((_, reject) => {
    timer = setTimeout(() => reject(new Error('tool timed out')), POLICY.toolTimeoutMs);
  });
  try {
    return await Promise.race([fn(), timeout]);
  } finally {
    clearTimeout(timer!);
  }
}
`;
}

// --- audit log -------------------------------------------------------------

function auditTs(): string {
  return `${HEADER}
import { appendFile } from 'node:fs/promises';
import { POLICY } from './policy.js';

/** One audit record per tool call. Append-only; never mutated. */
export interface AuditEntry {
  ts: string;
  tool: string;
  decision: 'allowed' | 'denied' | 'approval-required' | 'error';
  reason: string;
  durationMs?: number;
}

const LOG_PATH = process.env.HARNESS_AUDIT_LOG ?? '.harness/audit.log';

export async function audit(entry: AuditEntry): Promise<void> {
  if (!POLICY.auditLog) return;
  await appendFile(LOG_PATH, JSON.stringify(entry) + '\\n', 'utf-8').catch(() => {
    /* never let auditing crash a tool call */
  });
}
`;
}

// --- auth (remote only) ----------------------------------------------------

function authTs(): string {
  return `${HEADER}
// Remote (Streamable HTTP) auth. Bearer token by default; swap for OAuth/mTLS
// as your deployment requires. Local stdio mode does not import this file.

export interface AuthResult {
  ok: boolean;
  subject?: string;
  reason?: string;
}

export function authenticate(headers: Record<string, string | undefined>): AuthResult {
  const expected = process.env.HARNESS_MCP_TOKEN;
  if (!expected) {
    return { ok: false, reason: 'HARNESS_MCP_TOKEN not set — refusing unauthenticated remote MCP' };
  }
  const auth = headers['authorization'] ?? headers['Authorization'];
  if (!auth || !auth.startsWith('Bearer ')) {
    return { ok: false, reason: 'missing bearer token' };
  }
  const token = auth.slice('Bearer '.length);
  // Constant-time-ish compare; for production use a real timing-safe compare.
  if (token.length !== expected.length || token !== expected) {
    return { ok: false, reason: 'invalid token' };
  }
  return { ok: true, subject: 'bearer' };
}
`;
}

